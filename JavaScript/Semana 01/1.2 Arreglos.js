//while
let contador = 0;
while (contador <3 ){
    console.log(contador);      // 0, 1, 2
    contador++;
}
console.log("Fin del ciclo while");  //Fin del ciclo while

//do while
let conteo = 0 ;
do{
    console.log(conteo);     //0, 1, 2
    conteo++;
}while(conteo < 3);
console.log("Fin del ciclo do while"); //fin del ciclo do while

